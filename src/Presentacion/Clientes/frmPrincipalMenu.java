    private void lblPagosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblPagosMousePressed
        String estado = "";
        int id = 0;
        String SQL = "select estado from tblcaja order by idCaja desc limit 1";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            while(rs.next()){
                estado = rs.getString("estado");
            }
            if("Abierta".equals(estado)){
                frmGestionarPagos form = new frmGestionarPagos();
                form.setVisible(true);
                form.toFront();
                this.dispose();
            }else{
                frmAbrirCaja form = new frmAbrirCaja();
                form.setVisible(true);
                form.toFront();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_lblPagosMousePressed

