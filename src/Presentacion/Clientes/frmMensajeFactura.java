    private void lblReportePendienteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblReportePendienteMousePressed
        Map p = new HashMap();
        
        p.put("estado", "Pendiente");
        JasperReport report;
        JasperPrint print;
        
        try {
            report = JasperCompileManager.compileReport(new File("").getAbsolutePath()+"/src/Reportes/reporte_solicitud_pendiente.jrxml");
            print = JasperFillManager.fillReport(report, p, cn);
            JasperViewer view = new JasperViewer(print, false);
            view.setTitle("Reporte de Solicitudes Pendientes");
            view.setVisible(true);
        } catch (JRException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_lblReportePendienteMousePressed

