    private void tblTrabajadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTrabajadoresMouseClicked
        int fila = tblTrabajadores.rowAtPoint(evt.getPoint());
        nrodeCedula = tblTrabajadores.getValueAt(fila, 3).toString();
        idTrabajador = lht.traerIdTrabajador(nrodeCedula);
        nombres = tblTrabajadores.getValueAt(fila, 1).toString();
        apellidos = tblTrabajadores.getValueAt(fila, 2).toString();
        idCuadrilla = lht.traerIdCuadrilla(nrodeCedula);
        cuadrilla = tblTrabajadores.getValueAt(fila, 4).toString();
        idPuesto = lht.traerIdPuesto(nrodeCedula);
        puesto = tblTrabajadores.getValueAt(fila, 5).toString();
        fechaEntrada = tblTrabajadores.getValueAt(fila, 6).toString();
        horaEntrada = tblTrabajadores.getValueAt(fila, 7).toString();
        idDetalleCuadrilla = lht.traerIdDetalleCuadrilla(nrodeCedula);
        btnMarcarSalida.setVisible(true);
        btnMarcarSalidaAtras.setVisible(true);
    }//GEN-LAST:event_tblTrabajadoresMouseClicked

