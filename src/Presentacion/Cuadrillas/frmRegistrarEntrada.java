    void buscarTrabajador(String cedula) {
        int id = lht.traerIdTrabajador(cedula);
        int idDetalle = lht.traerIdDetalleCuadrilla(cedula);
        String nombre = lht.traerNombreTrabajador(cedula);
        String apellido = lht.traerApellidoTrabajador(cedula);
        String cedulaIdent = lht.traerCedulaIdentidad(cedula);
        String puesto = lht.traerPuesto(cedula);
        int idPuesto = lht.traerIdPuesto(cedula);
        int idCuadrilla = lht.traerIdCuadrilla(cedula);
        String Cuadrilla = lht.traerCuadrilla(cedula);

        System.out.println(id);
        System.out.println(idDetalle);
        System.out.println(nombre);
        System.out.println(apellido);
        System.out.println(cedulaIdent);
        System.out.println(idPuesto);
        System.out.println(puesto);
        System.out.println(idCuadrilla);
        System.out.println(Cuadrilla);

        txtIdTrabajador.setText(String.valueOf(id));
        txtNombres.setText(nombre);
        txtApellidos.setText(apellido);
        txtIdPuesto.setText(String.valueOf(idPuesto));
        txtPuesto.setText(puesto);
        txtCedula.setText(cedulaIdent);
        txtIdCuadrilla.setText(String.valueOf(idCuadrilla));
        txtCuadrilla.setText(Cuadrilla);
        txtIdDetalleCuadrilla.setText(String.valueOf(idDetalle));
    }

