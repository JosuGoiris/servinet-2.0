    private void btnGuardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMousePressed
        DHorarioTrabajo dht = new DHorarioTrabajo();
        LHorarioTrabajo fun = new LHorarioTrabajo();

        dht.setHoraEntrada(Time.valueOf(txtHora.getText()));
        int año = jdFechaSolicitud.getCalendar().get(Calendar.YEAR);
        int mes = jdFechaSolicitud.getCalendar().get(Calendar.MONTH);
        int dia = jdFechaSolicitud.getCalendar().get(Calendar.DAY_OF_MONTH);
        dht.setFechaEntrada(new Date((año - 1900), mes, dia));

        dht.setDetallecuadrillaId(Integer.parseInt(txtIdDetalleCuadrilla.getText()));

        fun.insertarHorario(dht);
    }//GEN-LAST:event_btnGuardarMousePressed

    private void txtCedulaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedulaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if ("".equals(txtCedula.getText())) {
                txtCedula.setBackground(Color.red);
                JOptionPane.showMessageDialog(null, "La cédula es necesaria para buscar al trabajador");
            } else {
                String cedula = txtCedula.getText();
                buscarTrabajador(cedula);
            }
        }
    }//GEN-LAST:event_txtCedulaKeyPressed

