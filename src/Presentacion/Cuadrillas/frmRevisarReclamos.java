    private void tblTrabajosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTrabajosMouseClicked
        int fila = tblTrabajos.rowAtPoint(evt.getPoint());
        idTrabajosReclamos = Integer.parseInt(tblTrabajos.getValueAt(fila, 0).toString());
        id = tblTrabajos.getValueAt(fila, 0).toString();
        idTrabajos = lt.traerIdTrabajosConReclamos(idTrabajosReclamos);
        idReclamos = lt.traerIdReclamos(idTrabajosReclamos);
        detalle = lt.traerDetalle(idReclamos);
        idCliente = lt.traerIdClienteConReclamos(idReclamos);
        nombres = tblTrabajos.getValueAt(fila, 1).toString();
        apellidos = tblTrabajos.getValueAt(fila, 2).toString();
        cedula = tblTrabajos.getValueAt(fila, 3).toString();
        fechaReclamo = Date.valueOf(tblTrabajos.getValueAt(fila, 4).toString());
        int idDetalle = lt.traerIdDetalleServicio(idCliente);
        idServicio = lt.traerIdServicio(idDetalle);
        nombreServicio = tblTrabajos.getValueAt(fila, 5).toString();
        cuadrilla = tblTrabajos.getValueAt(fila, 6).toString();
        idCuadrilla = lt.traerIdCuadrilla(cuadrilla);
    }//GEN-LAST:event_tblTrabajosMouseClicked

