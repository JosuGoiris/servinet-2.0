    private void btnMostrarServiciosActivosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMostrarServiciosActivosMousePressed
        if (lblAceptado.isVisible()) {
            lblAceptado.setVisible(false);
            lblrevertir.setVisible(true);
            mostrarReclamosAtendidos("");
            ordenartamaños();
            btnEditarSombra.setVisible(true);
            btnEditarSolicitud.setVisible(true);
            btnEliminarSombra.setVisible(true);
            btnEliminarTrabajo.setVisible(true);
        } else {
            lblAceptado.setVisible(true);
            lblrevertir.setVisible(false);
            mostrarReclamosPendientes("");
            ordenartamaños();
            btnEditarSombra.setVisible(false);
            btnEditarSolicitud.setVisible(false);
            btnEliminarSombra.setVisible(false);
            btnEliminarTrabajo.setVisible(false);
        }
    }//GEN-LAST:event_btnMostrarServiciosActivosMousePressed

